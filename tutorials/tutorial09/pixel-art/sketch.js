function drawPixelArt(grid, x, y, pixel) {
    let i = 0;
    while (i < grid.length) {
        drawRow(grid[i], x, y, pixel);
        i++;
        y += pixel;
    } 
}

function drawRow (row, topX, topY, pixelWidth) {
    // row + (0. 1, 1, 2, 1, 0)
    // 0 -> transparent
    // 1 -> hotpink
    // 2 -> blak
    // 3 -> white

    let i = 0;
        while (i < row.length) {
        if (row[i] == 0) {
            noFill();
        } else if (row[i] == 1) {
            fill('magenta');
        } else if (row[i] == 2) {
            fill('navy'); 
        } else {
            fill('white');
        }

        square(topX, topY, pixelWidth)
        topX += pixelWidth;
        i++;
    }     
}


